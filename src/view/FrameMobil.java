    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Save CSV File");
    fileChooser.setFileFilter(new FileNameExtensionFilter("CSV files (*.csv)", "csv"));
    
    if (fileChooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
        File file = fileChooser.getSelectedFile();
        if (!file.getName().toLowerCase().endsWith(".csv")) {
            file = new File(file.getParentFile(), file.getName() + ".csv");
        }
        
        try {
            exportToCSV(file);
            JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke CSV");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error exporting to CSV: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnCetakActionPerformed

    private void exportToCSV(File file) throws Exception {
    try (FileWriter fw = new FileWriter(file);
         BufferedWriter bw = new BufferedWriter(fw)) {
        
        bw.write("ID Mobil,Merk,Model,Tahun,Plat Nomor,Tarif per Hari,Status");
        bw.newLine();
        
        String sql = "SELECT * FROM mobil";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(sql);
        
        while (rs.next()) {
            StringBuilder line = new StringBuilder();
            line.append(escapeCSV(rs.getString("id_mobil"))).append(",");
            line.append(escapeCSV(rs.getString("merk"))).append(",");
            line.append(escapeCSV(rs.getString("model"))).append(",");
            line.append(rs.getInt("tahun")).append(",");
            line.append(escapeCSV(rs.getString("plat_nomor"))).append(",");
            line.append(rs.getBigDecimal("tarif_per_hari")).append(",");
            line.append(escapeCSV(rs.getString("status")));
            bw.write(line.toString());
            bw.newLine();
        }
        
        rs.close();
        stmt.close();
    }
}

private String escapeCSV(String value) {
    if (value == null) {
        return "";
    }
    if (value.contains("\"") || value.contains(",") || value.contains("\n")) {
        return "\"" + value.replace("\"", "\"\"") + "\"";
    }
    return value;
}

