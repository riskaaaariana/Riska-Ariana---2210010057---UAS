   private void loadData() {
        try {
            String sql = "SELECT * FROM mobil";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Mobil");
            model.addColumn("Merk");
            model.addColumn("Model");
            model.addColumn("Tahun");
            model.addColumn("Plat Nomor");
            model.addColumn("Tarif/Hari");
            model.addColumn("Status");
            
            while (rs.next()) {
                Object[] row = new Object[7];
                row[0] = rs.getString("id_mobil");
                row[1] = rs.getString("merk");
                row[2] = rs.getString("model");
                row[3] = rs.getInt("tahun");
                row[4] = rs.getString("plat_nomor");
                row[5] = rs.getBigDecimal("tarif_per_hari"); 
                row[6] = rs.getString("status");
                model.addRow(row);
            }
            
            tblMobil.setModel(model);
            rs.close();
            stmt.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }
   
 
